	function RenderListRow() {
		global $conn, $Security;

		// course_outline_id
		$this->course_outline_id->ViewValue = $this->course_outline_id->CurrentValue;
		$this->course_outline_id->CssStyle = "";
		$this->course_outline_id->CssClass = "";
		$this->course_outline_id->ViewCustomAttributes = "";

		// course_code
		if (!is_null($this->course_code->CurrentValue)) {
			$sSqlWrk = "SELECT `course_code`, `name` FROM `courses` WHERE `course_code` = '" . ew_AdjustSql($this->course_code->CurrentValue) . "'";
			$sSqlWrk .= " ORDER BY `name` Asc";
			$rswrk = $conn->Execute($sSqlWrk);
			if ($rswrk) {
				if (!$rswrk->EOF) {
					$this->course_code->ViewValue = $rswrk->fields('course_code');
					$this->course_code->ViewValue .= ew_ValueSeparator(0) . $rswrk->fields('name');
				}
				$rswrk->Close();
			} else {
				$this->course_code->ViewValue = $this->course_code->CurrentValue;
			}
		} else {
			$this->course_code->ViewValue = NULL;
		}
		$this->course_code->CssStyle = "";
		$this->course_code->CssClass = "";
		$this->course_code->ViewCustomAttributes = "";

		// Title
		$this->Title->ViewValue = $this->Title->CurrentValue;
		$this->Title->CssStyle = "";
		$this->Title->CssClass = "";
		$this->Title->ViewCustomAttributes = "";

		// Duration
		$this->Duration->ViewValue = $this->Duration->CurrentValue;
		$this->Duration->CssStyle = "";
		$this->Duration->CssClass = "";
		$this->Duration->ViewCustomAttributes = "";

		// course_outline_id
		$this->course_outline_id->HrefValue = "";

		// course_code
		$this->course_code->HrefValue = "";

		// Title
		$this->Title->HrefValue = "";

		// Duration
